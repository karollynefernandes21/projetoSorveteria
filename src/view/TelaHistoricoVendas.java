    private void btnPesquisarVendasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarVendasActionPerformed
        try {
            // Pega as datas dos JTextFields
            String textoDataInicio = formattedTxtDataInicio.getText().trim();
            String textoDataFim = formattedTxtDataFinal.getText().trim();

            // Converte as datas para LocalDateTime
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDateTime dataInicio = LocalDate.parse(textoDataInicio, formatter).atStartOfDay();
            LocalDateTime dataFim = LocalDate.parse(textoDataFim, formatter).atTime(23, 59, 59);

            // Consulta as vendas no período
            VendasDao dao = new VendasDao();
            ArrayList<Vendas> lista = dao.consultarVendasPeriodo(dataInicio, dataFim);

            // Atualiza a tabela com os resultados encontrados
            DefaultTableModel modelo = (DefaultTableModel) tblVendas.getModel();
            modelo.setRowCount(0); // limpa a tabela

            for (Vendas v : lista) {
                modelo.addRow(new Object[]{
                    v.getId(),
                    v.getIdSorvete(),
                    v.getQuantidadeSorvete(),
                    v.getFormaPagamento(),
                    v.getDataHoraVenda().format(DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm")),
                    v.getValorTotal()
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao pesquisar vendas por período: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnPesquisarVendasActionPerformed

